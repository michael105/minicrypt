static void
SHA512_Transform(uint64_t *state, const uint8_t block[128], uint64_t W[80],
                 uint64_t S[8])
{
    int i;

    be64dec_vect(W, block, 128);
    memcpy(S, state, 64);
	 for (i = 0; i < 80; i += 16) {
		 for ( int a=0; a<16; a++ ){
			 RNDr(S, W, a, i); 
		 }
		 if (i == 64) {
			 break;
		 }
		 for ( int a=0; a<16; a++ ){
			 MSCH(W, a, i);
		 }
	 }
	 for (i = 0; i < 8; i++) {
		 state[i] += S[i];
	 }
}
